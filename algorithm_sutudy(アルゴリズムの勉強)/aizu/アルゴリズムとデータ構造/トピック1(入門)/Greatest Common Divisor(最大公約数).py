

# 最大公約数
# ==========================================================================================================================

# ２つの自然数 x, y を入力とし、それらの最大公約数を求めるプログラムを作成してください。

# ２つの整数 x と y について、x ÷ d と y ÷ d の余りがともに 0 となる d のうち最大のものを、x と y の最大公約数（Greatest Common Divisor）と言います。例えば、35 と14 の最大公約数 gcd (35, 14) は 7 となります。これは、35 の約数{1, 5, 7, 35}、14 の約数 {1, 2, 7, 14} の公約数 {1, 7} の最大値となります。


# 入力
# ==========================================================================================================================
# x と y が１つの空白区切りで１行に与えられます。

# 出力
# ==========================================================================================================================
# 最大公約数を１行に出力してください。







# 入出力例
# ==========================================================================================================================
# <in>
# 147 105
# <out>
# 21


# 取得
# ==========================================================================================================================
# 形式
# A B

# Sample
# 5 10

# コード１
A, B = map(int, input().split())


# 基本形 ※実際に動きます。
# ==========================================================================================================================
def gcd(m, n):
    r = m % n
    return gcd(n, r) if r else n
print(gcd(*map(int, input().split())))

# 解説
# ==========================================================================================================================

# 計算速度 ※実際に動きます。
# ==========================================================================================================================
import math
a,b=map(int,input().split())
print(math.gcd(a,b))

# メモリ
# ==========================================================================================================================

# コードサイズ ※実際に動きます。
# ==========================================================================================================================
a,b=map(int,input().split())
while b: a,b=b,a%b
print(a)









# 汎用モデル : 速度優先
# ==========================================================================================================================

"""
args
---
a : int
    対象の値
b : int
    対象の値

return
---
 : int
    最大公約数
"""
import math
def main(a,b):
    return math.gcd(a,b)

test_data = 100
test_data_1 = 200

ancer_data = main(
    a = test_data,
    b = test_data_1
)
print(ancer_data)


# 汎用モデル : ライブラリ無しver
# ==========================================================================================================================

"""
args
---
a : int
    対象の値
b : int
    対象の値

return
---
 : int
    最大公約数
"""
def main(a,b):
    while b: a,b=b,a%b
    return a

